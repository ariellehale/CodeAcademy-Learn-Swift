// Gigi is 8 years old

var dogAge: Double = 8

var earlyYears: Double
var laterYears: Double
var humanYears: Double

// First two years of a dog's life
earlyYears = 21

// Each of the follow years count as 4 human years
laterYears = (dogAge - 2) * 4

humanYears = earlyYears + laterYears

print("My name is Gigi! Ruff ruff, I am \(humanYears) years old in human years.")